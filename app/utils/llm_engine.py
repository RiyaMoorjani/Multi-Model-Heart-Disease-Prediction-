import os
import json
import http.client
from typing import Dict, Any, List

class ClinicalReportingEngine:
    def __init__(self, api_key: str = None, model: str = "llama-3.1-8b-instant"):
        self.api_key = api_key or os.getenv("GROQ_API_KEY", "")
        self.model = model or os.getenv("GROQ_MODEL_NAME", "llama-3.1-8b-instant")
        self.host = "api.groq.com"
        self.path = "/openai/v1/chat/completions"

    def generate_prompt(self, tabular_results: Dict[str, Any], vision_results: Dict[str, Any]) -> str:
        """
        Creates the clinical narrative context prompt containing tabular data,
        SHAP explainability findings, and ECG classification results.
        """
        risk_prob = tabular_results.get("risk_probability", 0.0)
        shap_agg = tabular_results.get("aggravating_features", [])
        
        ecg_prob = vision_results.get("ecg_risk_probability", 0.0)
        ecg_status = vision_results.get("ecg_status", "Unknown")
        
        # Compile SHAP aggravators
        agg_list = []
        for feat in shap_agg:
            name = feat.get("pretty_name", feat.get("feature", ""))
            val = feat.get("shap_value", 0.0)
            agg_list.append(f"- {name} (SHAP positive contribution: +{val:.3f})")
            
        agg_str = "\n".join(agg_list) if agg_list else "- None identified above threshold (>0.05 SHAP)"
        
        prompt = f"""
[SYSTEM ROLE]
You are a Senior Cardiologist and Medical AI Architect. Translate the patient's multimodal diagnostic findings into a structured, highly professional clinical report summary.

[PATIENT MULTIMODAL DIAGNOSTIC DATA]
1. Vitals/Tabular Heart Disease Probability: {risk_prob * 100:.2f}%
2. Key Aggravating Clinical Factors (SHAP Values > 0.05):
{agg_str}
3. Visual ECG Report Classification: {ecg_status} (Probability: {ecg_prob * 100:.2f}%)

[INSTRUCTIONS]
Write a structured, professional clinical summary report based on the above details. The report must contain:
- A summary of the patient's heart disease risk profile combining both clinical vitals and visual ECG waveform classifications.
- Analysis of the key aggravating clinical risk factors identified by the tabular layer.
- Clinical interpretation of the visual ECG report.
- Next steps/recommendations for clinical confirmation (e.g., echocardiogram, stress test, or cardiologist consultation).

[STRICT PROMPT GUARDRAIL & DISCLAIMER]
The output report MUST include the following exact medical disclaimer verbatim:
"DISCLAIMER: This report is generated by an artificial intelligence interpretive aid and does not constitute a final legal or medical diagnosis. All findings must be reviewed and verified by a licensed healthcare professional prior to clinical decision-making."

Provide the report in professional Markdown format.
"""
        return prompt

    def generate_report(self, tabular_results: Dict[str, Any], vision_results: Dict[str, Any]) -> str:
        """
        Sends the clinical data prompt to the Groq LLM endpoint via http.client.
        Falls back to a robust local clinical logic report if no API key is present.
        """
        prompt = self.generate_prompt(tabular_results, vision_results)
        
        if not self.api_key:
            return self._generate_fallback_report(tabular_results, vision_results)
            
        payload = {
            "model": self.model,
            "messages": [
                {"role": "system", "content": "You are a specialized cardiological reporting assistant."},
                {"role": "user", "content": prompt}
            ],
            "temperature": 0.2,
            "max_tokens": 1024
        }
        
        payload_bytes = json.dumps(payload).encode("utf-8")
        
        headers = {
            "Authorization": f"Bearer {self.api_key}",
            "Content-Type": "application/json",
            "Accept": "application/json"
        }
        
        conn = http.client.HTTPSConnection(self.host, timeout=10)
        try:
            conn.request("POST", self.path, body=payload_bytes, headers=headers)
            response = conn.getresponse()
            response_data = response.read()
            
            if response.status != 200:
                print(f"HTTP Connection Error: Status {response.status}, Details: {response_data.decode('utf-8')}")
                return self._generate_fallback_report(tabular_results, vision_results, error_msg=f"HTTP {response.status}")
                
            json_res = json.loads(response_data.decode("utf-8"))
            return json_res["choices"][0]["message"]["content"]
            
        except Exception as e:
            print(f"Failed to generate clinical report via Groq: {str(e)}")
            return self._generate_fallback_report(tabular_results, vision_results, error_msg=str(e))
        finally:
            conn.close()

    def _generate_fallback_report(self, tabular_results: Dict[str, Any], vision_results: Dict[str, Any], error_msg: str = None) -> str:
        risk_prob = tabular_results.get("risk_probability", 0.0)
        shap_agg = tabular_results.get("aggravating_features", [])
        ecg_prob = vision_results.get("ecg_risk_probability", 0.0)
        ecg_status = vision_results.get("ecg_status", "Unknown")
        
        agg_list = []
        for feat in shap_agg:
            name = feat.get("pretty_name", feat.get("feature", ""))
            val = feat.get("shap_value", 0.0)
            agg_list.append(f"- **{name}**: Contributes positively to cardiorisk (SHAP value: +{val:.3f})")
        agg_str = "\n".join(agg_list) if agg_list else "- No features exceeded the aggravation threshold (> 0.05 SHAP)."
        
        notice = ""
        if error_msg:
            notice = f"> *Note: API connection failed ({error_msg}). Generating report using local clinical rules.*\n\n"
        else:
            notice = f"> *Note: GROQ_API_KEY environment variable is missing. Generating report using local clinical rules.*\n\n"
            
        overall_risk = "Elevated" if (risk_prob > 0.5 or ecg_prob > 0.5) else "Low"
        
        report = f"""# CLINICAL ASSESSMENT SUMMARY REPORT
{notice}
## 1. Patient Risk Overview
- **Vitals-based Cardiovascular Disease Risk**: {risk_prob * 100:.2f}%
- **Visual ECG Waveform Analysis**: {ecg_status} (Calculated risk: {ecg_prob * 100:.2f}%)
- **Unified Diagnostic Assessment**: **{overall_risk} Risk Profile**

## 2. Key Aggravating Vitals (SHAP Analytics)
The tabular model identified the following clinical parameters as significantly driving up the patient's cardiac risk profile:
{agg_str}

## 3. ECG Electrocardiogram Analysis
- The OpenCV grid elimination successfully isolated the raw waveforms.
- The computer vision network classified the signal as **{ecg_status}**.
- This indicates {"evidence of morphological abnormalities (such as ST segment alterations or bundle branch blocks) that require clinical review." if ecg_prob > 0.5 else "no significant visual evidence of diagnostic abnormalities in the provided trace."}

## 4. Clinical Recommendations & Next Steps
1. **Cardiologist Consultation**: Refer to a cardiologist for formal review.
2. **Diagnostic Tests**: Schedule a 12-lead diagnostic ECG and a comprehensive echocardiogram to verify ventricular function.
3. **Continuous Monitoring**: Recommend monitoring resting blood pressure and lipid panel adjustments based on SHAP findings.

---

DISCLAIMER: This report is generated by an artificial intelligence interpretive aid and does not constitute a final legal or medical diagnosis. All findings must be reviewed and verified by a licensed healthcare professional prior to clinical decision-making.
"""
        return report
